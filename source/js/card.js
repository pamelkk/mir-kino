const cardTemplateElement = document.querySelector(`.card-template`).content;

let horrorWrapper = document.querySelector(".main__catalog-category--horror");
let comedyWrapper = document.querySelector(".main__catalog-category--comedy");
let actionWrapper = document.querySelector(".main__catalog-category--action");
let documentaryWrapper = document.querySelector(".main__catalog-category--documentary");

const cardListElementHorror = horrorWrapper.querySelector(`ul`);
const cardListElementComedy = comedyWrapper.querySelector(`ul`);
const cardListElementAction = actionWrapper.querySelector(`ul`);
const cardListElementDocumentary = documentaryWrapper.querySelector(`ul`);

function createCard(item) {
const cardElement = cardTemplateElement.cloneNode(true);

cardElement.querySelector(`a`).href = item.link;
cardElement.querySelector(`h2`).textContent = item.header;
cardElement.querySelector(`img`).src = item.image;
cardElement.querySelector(`img`).alt = item.altText;
cardElement.querySelector(`.choice__year`).textContent = item.year;
cardElement.querySelector(`.choice__country`).textContent = item.country;
cardElement.querySelector(`.choice__genre`).textContent = item.genre;
cardElement.querySelector(`.choice__description`).textContent = item.description;

if(cardElement.querySelector(`.choice__genre`).textContent == "ужасы") {
    cardListElementHorror.appendChild(cardElement);
} else if(cardElement.querySelector(`.choice__genre`).textContent == "комедия") {
    cardListElementComedy.appendChild(cardElement);
} else if(cardElement.querySelector(`.choice__genre`).textContent == "фантастика") {
    cardListElementAction.appendChild(cardElement);
} else if(cardElement.querySelector(`.choice__genre`).textContent == "документальный") {
    cardListElementDocumentary.appendChild(cardElement);
}

}

for (let card of cards) {
createCard(card);
}